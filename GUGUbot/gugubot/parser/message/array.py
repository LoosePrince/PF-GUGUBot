from typing import List, Dict

class ArrayHandler:
    @staticmethod
    def build(array: List[Dict[str, Dict[str, str]]]) -> List[Dict[str, Dict[str, str]]]:
        return array
    
    @staticmethod
    def parse(message: List[Dict[str, Dict[str, str]]]) -> List[Dict[str, Dict[str, str]]]:
        return message
